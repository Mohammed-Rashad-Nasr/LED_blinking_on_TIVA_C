void MCU_VoidClockInit(void)
{
	#if   CLOCK_OSC == MOSC
		MCU_RCC &= ~((0b11)<<5);
		MCU_RCC |=  ((0b00)<<5);
		CLR_BIT     (MCU_RCC,0);
	#elif CLOCK_OSC == PIOSC
		MCU_RCC &= ~((0b11)<<5);
		MCU_RCC |=  ((0b01)<<5);
	#elif CLOCK_OSC == PIOSCDIV4
		MCU_RCC &= ~((0b11)<<5);
		MCU_RCC |=  ((0b10)<<5);
	#elif CLOCK_OSC == LFIOSC
		MCU_RCC &= ~((0b11)<<5);
		MCU_RCC |=  ((0b11)<<5);
	#endif
	
	#if PLL_USE == TRUE 
		SET_BIT            (MCU_RCC,11);
		CLR_BIT            (MCU_RCC,22);
		
		CLR_BIT            (MCU_RCC,13);
		
		MCU_RCC &= ~((0b11111)    <<10);
		MCU_RCC |=  ((CRYSTAL_VAL)<<10);
		
		MCU_RCC &= ~((0b1111)     <<26);
		MCU_RCC |=  ((SYSDIV_VAL) <<26);
		
		SET_BIT            (MCU_RCC,22);
		
		while   (!(GET_BIT(MCU_RIS,6)));
		
		CLR_BIT            (MCU_RCC,11);
		
	#else
		/******NOT COMPLETE********/
	#endif
	
	
}

void MCU_VoidGPIOEnable (u8 copy_u8Port)
{
	SET_BIT(MCU_RCGCGPIO,copy_u8Port);
}

void MCU_VoidTimerEnable (u8 copy_u8Timer)
{
	SET_BIT(MCU_RCGCTIMER,copy_u8Timer);
}

